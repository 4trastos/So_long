void	reset_road(char  **map)
{
	int	y;
	int	x;

	y = 0;
	while (map[y] != NULL)
	{
		x = 0;
		while (map[y][x] != '\0')
		{
			if (map[y][x] == 'F' || map[y][x] == 'M' || map[y][x] == 'Z')
			{
				map[y][x] = '0';
				i++;
			}
			i++;
		}
		y++;
	}
}
